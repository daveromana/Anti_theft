public class Module {
    private String module_name;
    private int module_img;
    private String module_function;

    public String getModule_name() {
        return module_name;
    }

    public void setModule_name(String module_name) {
        this.module_name = module_name;
    }

    public int getModule_img() {
        return module_img;
    }

    public void setModule_img(int module_img) {
        this.module_img = module_img;
    }

    public String getModule_function() {
        return module_function;
    }

    public void setModule_function(String module_function) {
        this.module_function = module_function;
    }
}
